<p><strong><u>FOR MAINTENANCE TEAM</u></strong></p>
<ol>
  <li>From the homepage, you should select the Maintenance Team  selection to log in.</li>
  <li>To add new debtors&rsquo; information, click on the &ldquo;Add New&rdquo;  button on the bottom left of your screen.</li>
  <li>Fill in the debtors&rsquo; information (ID, name, status,  etc..) in the text area and click &ldquo;Add&rdquo;.</li>
  <li>To edit a record, first you have to choose which record  you would like to edit. Next, click on the &ldquo;Edit&rdquo; button which can be found on  the right side (next to the delete button). Edit the information and click &ldquo;Save&rdquo;.</li>
  <li>To delete a record, you can find the delete button on the  right side of each record (beside the edit button).</li>
  <li>To search for a specific record, you first have to  identify which type of information you would like to search for (EG: debtors&rsquo;  name, statement, status). Then, on the left side of your screen, you type in a  specific keyword in the search bar you would like to search.</li>
  <li>You can also edit or delete a specific record that you  search for.</li>
</ol>
